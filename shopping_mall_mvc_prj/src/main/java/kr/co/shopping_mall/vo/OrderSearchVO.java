package kr.co.shopping_mall.vo;

public class OrderSearchVO {
	private String division;
	private String searchValue;
	private String order_stat_cd;
	private String order_date1;
	private String order_date2;
	private String index;
	private String flag;
	
	public String getDivision() {
		return division;
	}
	public void setDivision(String division) {
		this.division = division;
	}
	public String getSearchValue() {
		return searchValue;
	}
	public void setSearchValue(String searchValue) {
		this.searchValue = searchValue;
	}
	public String getOrder_stat_cd() {
		return order_stat_cd;
	}
	public void setOrder_stat_cd(String order_stat_cd) {
		this.order_stat_cd = order_stat_cd;
	}
	public String getOrder_date1() {
		return order_date1;
	}
	public void setOrder_date1(String order_date1) {
		this.order_date1 = order_date1;
	}
	public String getOrder_date2() {
		return order_date2;
	}
	public void setOrder_date2(String order_date2) {
		this.order_date2 = order_date2;
	}
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
}
